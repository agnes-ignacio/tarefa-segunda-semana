// Crie um algoritmo que leia o valor de um jantar, calcule e informe
// o valor da taxa do garçom (10%) e o valor total a ser pago.

let valor = 300
let taxaGarcom = (300 / 100) * 10
let valorTotal = valor + taxaGarcom

console.log("O valor do jantar é de R$" + valor + ".")
